public class Tuple
{
    int x = 0;
    int y = 0;

    public Tuple(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int ax) {
        this.x = ax;
    }

    public void setY(int ay) {
        this.y = ay;
    }

}
