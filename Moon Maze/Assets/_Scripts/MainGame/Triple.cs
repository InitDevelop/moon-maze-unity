public class Triple
{
    int x = 0;
    int y = 0;
    int z = 0;

    public Triple(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public void setX(int ax) {
        this.x = ax;
    }

    public void setY(int ay) {
        this.y = ay;
    }

    public void setZ(int az) {
        this.z = az;
    }

}
